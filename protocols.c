static inline void
add_to_crc(uint8_t c, uint8_t *crc){
     c = c ^ (*crc >> (WIDTH - 8));
     *crc = crcTable[c] ^ (*crc << 8);
}


// ********************************************************************************************
// Packet scanner 

#define S_INIT   0 // Initialize the scanner
#define S_START  1 // Scan for the start character
#define S_LEN    2 // get message length
#define S_DATA   3 // read data
#define S_CKSUM  4 // read checksum and end char, validate cksum and delegate the result

#define C_START  0x01
#define C_END    0x00

// Maximum length for input message
#define MAXLEN   5


static uint8_t scanner_state=S_INIT;
static uint8_t datalen;
static uint8_t dataidx;
static uint8_t indata[MAXLEN];
static uint8_t rx_cksum;

uint8_t input_scanner(void){
     uint8_t c;
     uint8_t packet_ready=0;
     switch(scanner_state){
     case S_INIT:
	  break;
     case S_START:
	  if(uart_async_get_uint8(&c)){
	       if(c==C_START){
		    scanner_state=S_LEN;
		    rx_crc=0;
	       } else
		    scanner_state=S_START;
	  }
	  break;
     case S_LEN:
	  if(uart_async_get_uint8(&c)){
	       add_to_crc(c,&rx_crc);
	       datalen=c;
	       dataidx=0;
	       if(datalen<MAXLEN)
		    scanner_state=S_DATA;
	       else
		    scanner_state=S_START;
	  }
	  break;
     case S_DATA:
	  if(uart_async_get_uint8(&c)){
	       add_to_crc(c,&rx_crc);
	       if(datalen--)
		    indata[dataidx++]=c;
	       else {
		    scanner_state=S_CKSUM;
		    datalen=0;
	       }
	  }
	  break;
     case S_CKSUM:
	  if(uart_async_get_uint8(&c)){
	       switch(datalen++){
	       case 0:
		    // scan next packet if the cksum does not match
		    if(c!=rx_crc)
			 scanner_state=S_START;
		    break;
	       case 1:
		    // tell back that the packet is ready
		    if(c==C_END)
			 packet_ready=1;
		    scanner_state=S_START;
	       default:
		    scanner_state=S_INIT;
	       }
	  }
     default:
	  scanner_state=S_INIT;
     }
     return packet_ready;
}
		    
void process_packet(void){
     switch(indata[0]){
     case MMSG_POWER:
	  left_power_in=(int16_t)(indata[1]|(indata[2]<<8));
	  right_power_in=(int16_t)(indata[3]|(indata[4]<<8));
	  break;
     case MMSG_STOP:
     case MMSG_FAIL:
     case MMSG_CLEAR:
     case MMSG_TIMEOUT:
	  motor_msg=indata[0];
     }
}

